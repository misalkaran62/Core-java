import java.util.Scanner;
	public class PrintOneToN{
		public static void main(String[] args){
			Scanner sc = new Scanner(System.in);
		
			System.out.println("Enter number To Print :");
			int n = sc.nextInt();
	
			for( int i = 0; i < n; i++){
				System.out.println("Hello World !! ");
			}
	}
}
				