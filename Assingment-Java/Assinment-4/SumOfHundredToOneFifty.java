//Write a program to find sum of 100 to 150

		public class SumOfHundredToOneFifty{
			public static void main(String[] args){
				
				System.out.println("Sum Of 100 to 150 :");
				int sum = 0;
				for (int i = 100;i <= 150;i++){
					sum = sum + i;  //sum+=i;
				}
				
				System.out.println("Sum : " + sum);

	}
}
