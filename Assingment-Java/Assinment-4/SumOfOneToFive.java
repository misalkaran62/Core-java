//Write a program to find sum of 1 to 5 (sum of first 5 numbers)

		public class SumOfOneToFive{
			public static void main(String[] args){
				
				System.out.println("Sum Of 1 to 5 :");
				int sum = 0;
				for (int i = 1;i <= 5;i++){
					sum = sum + i;  //sum+=i;
				}
				
				System.out.println("Sum : " + sum);

	}
}
	